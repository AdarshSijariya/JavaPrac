class User {
    String name;
    int age;    

    private String mobile;
    private String email;

    public String address;
    public String occupation;

    private String getAddress() {
        return address;
    }

    private String getOccupation() {
        return occupation;
    }

    String getName() {
        return name;
    }

    int getAge() {
        return age;
    }

    public String getMobile() {
        return mobile;
    }

    public String getEmail() {
        return email;
    }
}
