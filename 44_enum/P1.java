class P1 {
    public static void main(String[] args) {
        char x = 'C';
        
        switch(x) {
            case 'A':
                System.out.println("A");
                break;
            case 'B':
                System.out.println("B");
                break;
            case 'C':
                System.out.println("C");
                break;
            default:
                System.out.println("D");
        }
    }    
}